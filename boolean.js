/* Boolean - Valores booleans normalmente só possuem duas opções, sendo elas: 0 e 1

No JavaScript, utilizamos o mesmo princípio, só que em vez de ser 0 e 1, são os elementos (true (1) or false(0)), atribuindo valor booleano fica:
(1) para verdadeiro e (0) para falso.

Quando utilizamos o elemento (= igual) é o mesmo que atribuir valor a algo. por exemplo:

let myNumberOne = 10
let myNumberTwo = 20

myNumberOne = myNumberTwo

console.log(myNumberOne)

Na função acima, estamos dizendo que a variável (myNumberOne) irá receber o valor que está na variável (myNumberTwo) no caso, a primeira variável não
possuirá o valor 10 e sim o valor 20, atribuído na função.

Ao colocar (== dois ou === três iguais) estamos fazendo uma comparação.

*/

let myNumberOne = 10
let myNumberTwo = 20


console.log(myNumberOne == myNumberTwo)
