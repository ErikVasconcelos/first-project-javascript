/* Operadore lógicos

&& = E

    true && true = true (Duas informações verdadeiras = verdadeiro)
    true && false = false (Uma de cada = falso)
    false && false = false (Duas informações falsas = falso)

|| = Ou

    true || true = true (Duas informações verdadeiras = verdadeiro)
    true || false = true (Uma de cada = verdadeiro)
    false || false = false (Duas informações falsas = falso)

! = Negação

    !true = false (Nega o valor, invertendo o resultado)
    !false = true (Nega o valor, invertendo o resultado)

    Por ex: O que necessitamos para realizar uma transferência bancária?

    - Ter saldo em conta
    - Conta desbloqueda
    - Destinatário correto

*/

